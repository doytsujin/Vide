    private void jComboBox3ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jComboBox3ActionPerformed
    {//GEN-HEADEREND:event_jComboBox3ActionPerformed
        if (jComboBox3.getSelectedIndex() == -1) return;

        File path = new File(de.malban.Global.mBaseDir+File.separator+ ((String)jComboBox3.getSelectedItem()));
        File files[];
        files = path.listFiles();
        if (files ==null) return;
        Vector<String> matches = new Vector<String>();
        Arrays.sort(files);
        for (int i = 0, n = files.length; i < n; i++)
        {
            String aName = files[i].toString();
            if (!files[i].isDirectory())
            {
                if (aName.indexOf("Deck.xml")!=-1)
                {
                    aName = de.malban.util.UtilityString.replace(aName, "Deck.xml", "");
                    int nameStart = aName.lastIndexOf(File.separator);
                    aName = aName.substring(nameStart+1);
                    matches.addElement(aName);
                }
            }
        }
        Collections.sort(matches, new Comparator<String>()
        {
            @Override
            public final int compare(String s1, String s2)
            {
                return (s1.compareTo(s2));
            }
        });
        jComboBox1.removeAllItems();
        for (int i = 0; i < matches.size(); i++)
        {
            String string = matches.elementAt(i);
            jComboBox1.addItem(string);
        }
    }//GEN-LAST:event_jComboBox3ActionPerformed


