    public void setImage(String imageName, boolean res)
    {
        mResize=res;
        if (mResize)
        {
            int w = mParent.getUsableFrameWidth();
            int h = mParent.getUsableFrameHeight();
            if (w==0) w = 1024;
            if (h==0) h = 768;
            image = de.malban.util.UtilityImage.loadImage(imageName);
            if (image != null)
            {
                Image titleImage = image.getScaledInstance(w, h, java.awt.Image.SCALE_SMOOTH);
                mIcon = new javax.swing.ImageIcon(titleImage);
            }
            title.setIcon(mIcon);
            title.setBounds(0, 0,w,h);
        }
        else
        {
            ImageIcon ic = new javax.swing.ImageIcon(imageName);
            while ((ic.getImageLoadStatus() != MediaTracker.ABORTED) &&
                   (ic.getImageLoadStatus() != MediaTracker.COMPLETE) &&
                   (ic.getImageLoadStatus() != MediaTracker.ERRORED) )
            {
                try
                {
                    wait(10);
                }catch (Throwable e){}
            }
            title.setIcon(ic);
        }

        validate();
        repaint();
    }

